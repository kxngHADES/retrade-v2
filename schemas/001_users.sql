CREATE TABLE IF NOT EXISTS users (
	uid BINARY(16) NOT NULL DEFAULT(UUID_TO_BIN(UUID())),
	firstName VARCHAR(255) NOT NULL,
	lastName VARCHAR(255) NOT NULL,
	email VARCHAR(255) NOT NULL,
	phoneNumber VARCHAR(20) NOT NULL,
	password VARCHAR(255) NOT NULL,
	is_phone_verified TINYINT(1) NOT NULL DEFAULT 0,
	is_email_verified TINYINT(1) NOT NULL DEFAULT 0,
	is_id_verified TINYINT(1) NOT NULL DEFAULT 0,
	profile_image_url VARCHAR(2048) NULL,
	rbac_role TINYINT(3) NOT NULL DEFAULT 0 COMMENT '0=buy only, 1=byt/sell, 2=admin, 3=superAdmin',
	is_banned TINYINT(1) NOT NULL DEFAULT 0,
	province VARCHAR(120) NULL,
	city VARCHAR(255) NULL,
	street_address VARCHAR(255) NULL,
	location_point POINT NULL SRID 4326,
	created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	last_login DATETIME NULL,
	CONSTRAINT pk_users PRIMARY KEY (uid),
	CONSTRAINT uq_users_email UNIQUE (email),
	CONSTRAINT uq_users_phone UNIQUE (phoneNumber)
) ENGINE=InnoDB;